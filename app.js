// Traditional Function:
function add (a,b){
  return a + b
}
function multiply (a,b){
  return a * b
}
function greet (name){
  return 'hello, ' + name;
}

// Arrow Function:
const  add =  (a,b) =>{
  return a + b
}
const multiply =  (a,b) =>{
  return a * b
}
const greet = (name) => {
  return 'hello, ' + name;
}
